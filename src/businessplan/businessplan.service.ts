  async save(dto: SaveBusinessPlanDto) {
    const userId = dto.userId ?? 'anonymous';
    // Replace previous plan for this user
    await this.planRepo.delete({ userId });

    const entity = this.planRepo.create({
      userId,
      projectName: dto.projectName,
      description: dto.description,
      valueProposition: dto.valueProposition,
      targetCustomers: dto.targetCustomers,
      costs: dto.costs,
      firstSteps: dto.firstSteps,
      sector: dto.sector ?? null,
    });
    await this.planRepo.save(entity);

    return { ok: true, id: entity.id };
  }

  async getByUser(userId: string) {
    return this.planRepo.findOne({
      where: { userId },
      order: { createdAt: 'DESC' },
    });
  }
